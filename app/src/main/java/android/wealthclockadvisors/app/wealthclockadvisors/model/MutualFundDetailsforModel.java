package android.wealthclockadvisors.app.wealthclockadvisors.model;

public class MutualFundDetailsforModel {

    public String AmcCode ;
    public String SchemeName ;
    public String SchemeType ;
    public String Plan;
    public String DividendDivision ;
    public String DividendOption ;
    public String Amount ;
    public String Folio ;
    public String PaymentMode ;


    public MutualFundDetailsforModel() {

    }

    public String getAmcCode() {
        return AmcCode;
    }

    public void setAmcCode(String amcCode) {
        AmcCode = amcCode;
    }

    public String getSchemeName() {
        return SchemeName;
    }

    public void setSchemeName(String schemeName) {
        SchemeName = schemeName;
    }

    public String getSchemeType() {
        return SchemeType;
    }

    public void setSchemeType(String schemeType) {
        SchemeType = schemeType;
    }

    public String getPlan() {
        return Plan;
    }

    public void setPlan(String plan) {
        Plan = plan;
    }

    public String getDividendDivision() {
        return DividendDivision;
    }

    public void setDividendDivision(String dividendDivision) {
        DividendDivision = dividendDivision;
    }

    public String getDividendOption() {
        return DividendOption;
    }

    public void setDividendOption(String dividendOption) {
        DividendOption = dividendOption;
    }

    public String getAmount() {
        return Amount;
    }

    public void setAmount(String amount) {
        Amount = amount;
    }

    public String getFolio() {
        return Folio;
    }

    public void setFolio(String folio) {
        Folio = folio;
    }

    public String getPaymentMode() {
        return PaymentMode;
    }

    public void setPaymentMode(String paymentMode) {
        PaymentMode = paymentMode;
    }
}
